public class URLData
{
    Integer _index;
    String _link;
    String _linkText;

    public Integer getIndex()
    {
        return _index;
    }

    public void setIndex(Integer index)
    {
        _index = index;
    }

    public String getLink()
    {
        return _link;
    }

    public void setLink(String link)
    {
        _link = link;
    }

    public String getLinkText()
    {
        return _linkText;
    }

    public void setLinkText(String linkText)
    {
        _linkText = linkText;
    }
}
